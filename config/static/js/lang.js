    apply: function (lang) {
        const dict = this.translations[lang] || this.translations.ru;
        // data-i18n -> textContent / innerHTML
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            const val = dict[key];
            if (val !== undefined) {
                if (el.hasAttribute('data-i18n-html')) el.innerHTML = val; else el.textContent = val;
            }
        });
        // data-i18n-placeholder -> set placeholder attribute
        document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
            const key = el.getAttribute('data-i18n-placeholder');
            const val = dict[key];
            if (val !== undefined) el.setAttribute('placeholder', val);
        });
        // data-i18n-title -> set title attribute
        document.querySelectorAll('[data-i18n-title]').forEach(el => {
            const key = el.getAttribute('data-i18n-title');
            const val = dict[key];
            if (val !== undefined) el.setAttribute('title', val);
        });
    }
